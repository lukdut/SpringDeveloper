CREATE TABLE BOOKS(ID UUID PRIMARY KEY, TITLE VARCHAR(255));

CREATE TABLE AUTHORS(ID UUID PRIMARY KEY, NAME VARCHAR(255));

CREATE TABLE GENRES(ID UUID PRIMARY KEY, NAME VARCHAR(255));

CREATE TABLE GENRES_BOOKS(BOOK_ID UUID, GENRE_ID UUID, FOREIGN KEY (BOOK_ID) REFERENCES BOOKS(ID), FOREIGN KEY (GENRE_ID) REFERENCES GENRES(ID));

CREATE TABLE AUTHORS_BOOKS(BOOK_ID UUID, AUTHOR_ID UUID, FOREIGN KEY (BOOK_ID) REFERENCES BOOKS(ID), FOREIGN KEY (AUTHOR_ID) REFERENCES AUTHORS(ID));